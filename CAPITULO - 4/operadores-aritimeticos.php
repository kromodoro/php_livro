		OPERADORES ARITMÉTICOS
		Utilizando esses operadores você poderá efetuar qualquer operação matemática com os dados do tipo numérico, como, por exemplo, somar, subtrair, multiplicar, dividir, etc.

		OPERADOR | OPERAÇÃO
			+	 | Adição
			-	 | Subtração
			*	 | Multiplicação
			/ 	 | Divisão
			% 	 | Resto da divisão

		O PHP possui também outros operadores aritméticos, que atuam em apenas um operando. Esses operadores são bastante úteis, pois nos permitem realiza de forma simples operações, como troca de sinal, incremento ou decremento de valores, etc.

		OPERADOR | OPERAÇÃO
		 -oper 	 | Troca o sinal do operando
		 ++oper  | Pré-incrementa. Primeiro incrementa o valor do operando e depois realiza a operação.
		 --oper  | Pré-decremento. Primeiro decrementa o valor do operando e depois realiza a operação.
		 ope++   | Pós-incremento. Priemiro realiza a operação e depois incrementa o operando.
		 oper--  | Pós-decremento. Priemiro realiza a operação e depois decrementa o operando.

		Os operadores mostrados na tabela também são conhecidos como operadores unários, pois necessitam apenas de um operando, ao contrário da adição, subtração e outras operações que necessitam de pelo menos dois operandos. 
