		OPERADORES BINÁRIOS
		Esses operadores atuam em um nível de abstração bem mais baixo: trabalham diretamente com os bits. Podem ser utilizados para fazer comparações binárias (bit a bit), inverter os bits de um operando, deslocar bits para direita (cada deslocamento para a direita equivale a uma divisão por 2) ou esquerda (cada deslocamento para a esquerda equivale a multiplicar o número por 2). Em alguns casos é interessante usar os operadores binários.

		OPERADOR | DESCRIÇÃO
		  ~op1   | Inverte os bits de op1
		 op1&op2 | Operação E(and) bit a bit
		 op1|op2 | Operação OU(or) bit a bit
		 op1^op2 | Operação OU EXCLUSIVO(xor)
		 op1>>n  | Desloca op1 N bits à direita
		 op1<<n  | Desloca op1 N bits à esquerda

