	/*
		OPERADORES DE ATRIBUIÇÃO
		Atribuição é o termo usado para representar a colocação de uma valor em uma variável. A variável que receberá a atribuição encontra-se sempre do lado esquerdo do operador, e esta recebe o valor gerado pela expressão ou operador que está a direita. Além disso temos diversas variações dos comandos de atribuição, que podemos utilizar para facilitar a programação. São operadores que, assim como os operadores de incremento (++) e decremento (--), serem para deixar o código mais simples e mais fácil de ser programado.

				OPERADOR   | DESCRIÇÃO
				op1 = op2  | op1 recebe o valor de op2.
				op1 += op2 | Equivale a op1 = op1 + op2.
				op1 -= op2 | Equivale a op1 = op1 - op2.
				op1 *= op2 | Equivale a op1 = op1 * op2.
				op1 /= op2 | Equivale a op1 = op1 / op2.
				op1 .= op2 | Concatenação: equivale a op1 = op1 . op2.
				op1 %= op2 | Equivale a op1 = op1 % op2.
				op1 <<= op2| Equivale a op1 = op1 << op2.
				op1 >>= op2| Equivale a op1 = op1 >> op2.
				op1 &= op2 | Equivale a op1 = op1 & op2.
				op1 |= op2 | Equivale a op1 = op1 | op2.
				op1 ^= op2 | Equivale a op1 = op1 ^ op2.

		DICA: Muitas vzes seus programas podem apresentar problemas devido à troca de operador de comparação (==) pelo de atribuição (=). O programa acaba gerando resultado incorreto, pois se queremos fazer uma comparação, por exemplo, entre as variáveis $a e $b, devemos usar a expressão $a == $b. Use com atenção esses operadores para evitar resultados errados na execução do programa.
	*/				
