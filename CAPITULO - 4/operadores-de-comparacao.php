	/*
		OPERADORES DE COMPARAÇÃO
		Também chamados de condicionais. São aqueles que executam comparações entre o valor de duas variáveis, ou de uma variável e um texto, ou uma variável e um número. Com eles podemos testar por exemplo, se uma variável possui um valor maior que outra ou se possui um valor maior que determinado número, ou se o retorno dado pela chamada de uma função é verdadeira ou falsa.

		OPERADOR   | DESCRIÇÃO
		op1 == op2 | Verdadeiro se op1 for igual a op2.
		op1 >= op2 | Verdadeiro se op1 for maior ou igual a op2.
		op1 <= op2 | Verdadeiro se op1 for menor ou igual a op2.
		op1 != op2 | Verdadeiro se op1 for diferente de op2.
		op1 <> op2 | também serve para representar diferença.
		op1 > op2  | Verdadeiro se op1 for maior que op2.
		op1 < op2  | Verdadeiro se op1 for menor que op2.

		"O operador == pode ser usado tanto na pomparação de números como n acomparação de textos, ao contrário de linguagens como C, que utilizam comandos específicos para comparação de dados alfanuméricos."
